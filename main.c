int findEndingOfShot(int arr[]) {
    
    int targetChange = 10;
    for (int i = 511; i > 0; i--) { //stops at 511 because i will use i+1
        
        //calc the change and make it positive
        int change = abs(arr[i-1] - arr[511]);
        if (change > targetChange) {
            return i;
        }
    }
    
    printf("No shot detencted!");
    return 0;
}

